package CompleteSearch;

import java.util.ArrayList;

public class Domain {
	
	ArrayList<Integer> variables;
	
	public Domain(ArrayList<Integer> vars){
		this.variables = vars;
	}

	public int getSize() {
		return variables.size();
	}

	public Integer getVariable(int i) {
		return variables.get(i);
	}
	
	
	
}
